%
% ** dQmbv_dvi(i,v) **
% Returns the derivative of the quaternion matrix with respect to the
% ith element of the rotation vector.
% - i is the index of the element of the rotation vector with respect
%   to which the derivative is to be performed.
% - v is a rotation vector.
%
% Copyright (c) 2006 James Diebel, Stanford University
%
function dQ_dv = dQmv_dvi(i,v)
dQ_dv = Qmq(gcvi(i,v));