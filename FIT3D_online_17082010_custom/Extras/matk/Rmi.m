%
% ** Rmi(a) **
% Returns the rotation matrix effecting a rotation about the ith-axis of
% magnitude a.
% - a is the angle of rotation.
%
% Copyright (c) 2006 James Diebel, Stanford University
%
function R = Rmi(i,a)
if i == 1
  R = Rm1(a);
elseif i == 2
  R = Rm2(a);
elseif i == 3
  R = Rm3(a);
else
  disp('Error, first arg must be 1, 2, or 3.');
end
